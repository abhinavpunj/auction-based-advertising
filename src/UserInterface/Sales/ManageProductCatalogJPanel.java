    private void refreshTable()
    {
        DefaultTableModel model = (DefaultTableModel) productCatalog.getModel();
        model.setRowCount(0);
        
        for(Product p : organization.getProductCatalog().getProductCatalog())
        {
            Object[] row = new Object[4];
            row[0] = p;
            row[1] = p.getModelNumber();
            row[2] = p.getPrice();
            row[3] = p.getAvail();
            
            model.addRow(row);
        }
    }

