    private void addjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addjButtonActionPerformed

        try
        {
            String prodName = prodNamejTextField.getText();
            int price = Integer.parseInt(pricejTextField.getText().trim());
            int quantity = Integer.parseInt(quantityjTextField.getText().trim());
            if(prodName.trim().isEmpty())
            {
                JOptionPane.showMessageDialog(null, "Please enter product name");
                return;
            }
            Product p = organization.getProductCatalog().addProduct();
            p.setProdName(prodName);
            p.setAvail(quantity);
            p.setPrice(price);
            refreshTable();
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null, "Please enter product details in correct format");
        }
    }//GEN-LAST:event_addjButtonActionPerformed


