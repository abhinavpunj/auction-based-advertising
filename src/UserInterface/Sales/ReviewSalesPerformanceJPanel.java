    private void adVsSalejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adVsSalejButtonActionPerformed
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        dataset.setValue(enterprise.getAccount().getTotalSpent(), "Advertisement Expense vs Total Sales", "Ad Expenses");
        dataset.setValue(organization.getProductCatalog().getTotalSales(), "Advertisement Expense vs Total Sales", "Total Product Sales");
              
        JFreeChart chart = ChartFactory.createBarChart("Advertisement Expense vs Total Sales", "", "Amount", dataset, PlotOrientation.VERTICAL,false, true, false);
        CategoryPlot plot = chart.getCategoryPlot();
        plot.setRangeGridlinePaint(Color.black);
        ChartFrame frame = new ChartFrame("Advertisement Expense vs Total Sales", chart);
        frame.setVisible(true);
        frame.setSize(400, 400);
    }//GEN-LAST:event_adVsSalejButtonActionPerformed

    private void clickToPurchasejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_clickToPurchasejButtonActionPerformed
        
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        dataset.setValue(enterprise.getConsumerClicks(), "Consumer Clicks vs Purchases", "Consumer Clicks");
        dataset.setValue(organization.getConsumerPurchases(), "Consumer Clicks vs Purchases", "Consumer Purchases");
              
        JFreeChart chart = ChartFactory.createBarChart("Consumer Clicks vs Purchases", "", "Click Count", dataset, PlotOrientation.VERTICAL,false, true, false);
        CategoryPlot plot = chart.getCategoryPlot();
        plot.setRangeGridlinePaint(Color.black);
        ChartFrame frame = new ChartFrame("Consumer Clicks vs Purchases", chart);
        frame.setVisible(true);
        frame.setSize(400, 400);
    }//GEN-LAST:event_clickToPurchasejButtonActionPerformed


