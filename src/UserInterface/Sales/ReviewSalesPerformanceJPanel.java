    private void populateTable()
    {
        DefaultTableModel model = (DefaultTableModel) reviewjTable.getModel();
        model.setRowCount(0);
        
        Object[] row = new Object[4];
        row[0] = enterprise.getConsumerClicks();
        row[1] = organization.getConsumerPurchases();
        row[2] = enterprise.getAccount().getTotalSpent();
        row[3] = organization.getProductCatalog().getTotalSales();
        
        model.addRow(row);
    }

