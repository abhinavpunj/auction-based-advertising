    private void submitjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitjButtonActionPerformed
        
        MarketingAgencyEnterprise marketingEnterprise = (MarketingAgencyEnterprise) marketingjComboBox.getSelectedItem();
        AdvertiserEnrollRequest request = new AdvertiserEnrollRequest();
        request.setMarketingEnterprise(marketingEnterprise);
        request.setStatus("Request Sent");
        request.setSender(account);
        request.setAdvertiserEnterprise(enterprise);
        
        Enterprise isEnterprise = null;
        for(Enterprise e : network.getEnterpriseDirectory().getEnterpriseList())
        {
            if(e instanceof MarketingAgencyEnterprise)
            {
                if(((MarketingAgencyEnterprise)e).equals(marketingEnterprise))
                {
                    isEnterprise = e;
                }
            }
        }
        
        if(isEnterprise != null)
        {
            isEnterprise.getWorkQueue().getWorkQueue().add(request);
            enterprise.getWorkQueue().getWorkQueue().add(request);
        }
        
        populateTable();
    }//GEN-LAST:event_submitjButtonActionPerformed


