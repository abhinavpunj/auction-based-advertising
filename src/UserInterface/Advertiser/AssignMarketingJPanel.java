    private void populateTable()
    {
        DefaultTableModel model = (DefaultTableModel) requestjTable.getModel();
        model.setRowCount(0);
        
        for(WorkRequest request : enterprise.getWorkQueue().getWorkQueue())
        {
            Object[] row = new Object[4];
            row[0] = ((AdvertiserEnrollRequest)request).getMarketingEnterprise();
            row[1] = request.getReceiver();
            row[2] = request;
            row[3] = request.getComments();
            
            model.addRow(row);
            if(request.getStatus().equalsIgnoreCase("Approved"))
                submitjButton.setEnabled(false);
        }
    }
    
    private void populateComboBox()
    {
        marketingjComboBox.removeAllItems();
        for(Enterprise e : network.getEnterpriseDirectory().getEnterpriseList() )
        {
            if(e instanceof MarketingAgencyEnterprise)
            {
                marketingjComboBox.addItem(e);
            }
        }
        
    }

