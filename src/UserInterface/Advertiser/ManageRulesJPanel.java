    private void updateAdSpacejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateAdSpacejButtonActionPerformed

        try
        {
            float minHeight = Float.parseFloat(minHeightjTextField.getText().trim());
            float minWidth = Float.parseFloat(minWidthjTextField.getText().trim());
            String position = (String) positionjComboBox.getSelectedItem();
            if(position.equalsIgnoreCase("select"))
            {
                JOptionPane.showMessageDialog(null, "Please select an Ad Position from the list");
                return;
            }
            
            enterprise.getRule().setMinHeight(minHeight);
            enterprise.getRule().setMinWidth(minWidth);
            enterprise.getRule().setPosition(position);
            
            populateAdSpaceDimensions();
            JOptionPane.showMessageDialog(null, "Ad Dimensions Added");
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null, "Please enter Ad Dimensions in correct format");
        }
    }//GEN-LAST:event_updateAdSpacejButtonActionPerformed

