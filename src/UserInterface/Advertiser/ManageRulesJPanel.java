    private void populateBrowsingHistoryList()
    {
        //DefaultListModel model = (DefaultListModel) browsingHistoryjList.getModel();
        DefaultListModel model = new DefaultListModel();
        model.clear();
        
        
        for(String browse : enterprise.getRule().getBrowsingHistory())
        {
            model.addElement(browse);
        }
        browsingHistoryjList.setModel(model);
        if(enterprise.getRule().isAllBrowsingHistory())
        {
            browsingHistoryjCheckBox.setSelected(true);
        }
        else
        {
            browsingHistoryjCheckBox.setSelected(false);
        }
    }
    
    private void populatePurchaseHistory()
    {
        //DefaultListModel model = (DefaultListModel) browsingHistoryjList.getModel();
        DefaultListModel model = new DefaultListModel();
        model.clear();
        
        for(String purchase : enterprise.getRule().getPurchaseHistory())
        {
            model.addElement(purchase);
        }
        purchaseHistoryjList.setModel(model);
        if(enterprise.getRule().isAllPurchasingHistory())
        {
            purchasingHistoryjCheckBox.setSelected(true);
        }
        else
        {
            purchasingHistoryjCheckBox.setSelected(false);
        }
    }
    
    private void populateClickHistory()
    {
        clickHistoryjTextField.setText(String.valueOf(enterprise.getRule().getClickHistory()));
    }
    
    private void populateAdSpaceDimensions()
    {
        minHeightjTextField.setText(String.valueOf(enterprise.getRule().getMinHeight()));
        minWidthjTextField.setText(String.valueOf(enterprise.getRule().getMinWidth()));
        positionjTextField.setText(enterprise.getRule().getPosition());
    }
    
    private void populateBidAmount()
    {
        bidAmountjTextField.setText(String.valueOf(enterprise.getRule().getBidAmount()));
    }
    
    private void populatePageHitRate()
    {
        pageHitRatejTextField.setText(String.valueOf(enterprise.getRule().getHitRate()));
    }

