    private void addPurchasejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addPurchasejButtonActionPerformed

        String purchaseElement = String.valueOf(purchaseHistoryjComboBox.getSelectedItem());
        if(purchaseElement.equalsIgnoreCase("select"))
        {
            JOptionPane.showMessageDialog(null, "Please select a value to add");
            return;
        }
        for(String purchase : enterprise.getRule().getPurchaseHistory())
        {
            if(purchase.equals(purchaseElement))
            {
                JOptionPane.showMessageDialog(null, "Category already added");
                return;
            }
        }
        enterprise.getRule().getPurchaseHistory().add(purchaseElement);
        if(purchasingHistoryjCheckBox.isSelected())
        {
            enterprise.getRule().setAllPurchasingHistory(true);
        }
        populatePurchaseHistory();
        JOptionPane.showMessageDialog(null, "Purchase History Added");
    }//GEN-LAST:event_addPurchasejButtonActionPerformed

