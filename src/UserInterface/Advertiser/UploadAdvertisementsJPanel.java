    private void browsejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_browsejButtonActionPerformed

        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        int result = fileChooser.showOpenDialog(this);
        try
        {
            if (result == JFileChooser.APPROVE_OPTION) 
            {
                File selectedFile = fileChooser.getSelectedFile();
                pathjTextField.setText(selectedFile.getAbsolutePath());

                BufferedImage image = null;
                BufferedImage outputImage = null;
                File file = new File(pathjTextField.getText());
                image = ImageIO.read(file);
                outputImage = new BufferedImage(adjLabel.getWidth(), adjLabel.getHeight(), image.getType());
                Graphics2D g2d = outputImage.createGraphics();
                g2d.drawImage(image, 0, 0, adjLabel.getWidth(), adjLabel.getHeight(), null);
                g2d.dispose();
                adjLabel.setIcon(new ImageIcon(outputImage));
            } 
        }
        catch (IOException ex) 
        {
            JOptionPane.showMessageDialog(null, "Advertisement not found");
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, "Not a valid Advertisement");
            pathjTextField.setText("");
            adjLabel.setIcon(null);
        }
    }//GEN-LAST:event_browsejButtonActionPerformed

