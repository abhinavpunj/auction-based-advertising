    private void populateTable()
    {
        DefaultTableModel model = (DefaultTableModel) adjTable.getModel();
        model.setRowCount(0);
        
        for(Advertisement ad : enterprise.getAdvertisementList())
        {
            Object[] row = new Object[2];
            row[0] = ad;
            row[1] = ad.getTimeOfDay();
            
            model.addRow(row);
        }
    }

