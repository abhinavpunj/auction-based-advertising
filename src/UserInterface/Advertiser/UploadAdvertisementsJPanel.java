    private void deletejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletejButtonActionPerformed
        
        int selectedRow = adjTable.getSelectedRow();
        if(selectedRow < 0)
        {
            JOptionPane.showMessageDialog(null, "Please select an advertisement from the table");
            return;
        }
        Advertisement ad = (Advertisement) adjTable.getValueAt(selectedRow, 0);
        enterprise.getAdvertisementList().remove(ad);
        JOptionPane.showMessageDialog(null, "Advertisement removed");
        populateTable();
    }//GEN-LAST:event_deletejButtonActionPerformed

    private void addjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addjButtonActionPerformed
        
        String url = pathjTextField.getText().trim();
        String time = (String) timejComboBox.getSelectedItem();
        if(url.trim().isEmpty() || time.equalsIgnoreCase("select"))
        {
            JOptionPane.showMessageDialog(null, "Please enter both the values");
            return;
        }
        for(Advertisement ad : enterprise.getAdvertisementList())
        {
            if(ad.getTimeOfDay().equalsIgnoreCase(time))
            {
                JOptionPane.showMessageDialog(null, "Advertisement already added for " + time);
                return;
            }
        }
        enterprise.uploadAdvertisement(url, time);
        JOptionPane.showMessageDialog(null, "Advertisement uploaded");
        populateTable();
    }//GEN-LAST:event_addjButtonActionPerformed

