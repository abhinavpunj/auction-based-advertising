    private void addBrowsingjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBrowsingjButtonActionPerformed
        
        String browsingElement = String.valueOf(browsingHistoryjComboBox.getSelectedItem());
        if(browsingElement.equalsIgnoreCase("select"))
        {
            JOptionPane.showMessageDialog(null, "Please select a value to add");
            return;
        }
        for(String browse : consumer.getBehaviorPattern().getBrowsingHistory())
        {
            if(browse.equals(browsingElement))
            {
                JOptionPane.showMessageDialog(null, "Category already added");
                return;
            }
        }
        consumer.getBehaviorPattern().getBrowsingHistory().add(browsingElement);
        populateBrowsingHistoryList();
        JOptionPane.showMessageDialog(null, "Browsing History Added");
    }//GEN-LAST:event_addBrowsingjButtonActionPerformed

    private void addPurchasejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addPurchasejButtonActionPerformed
        
        String purchaseElement = String.valueOf(purchaseHistoryjComboBox.getSelectedItem());
        if(purchaseElement.equalsIgnoreCase("select"))
        {
            JOptionPane.showMessageDialog(null, "Please select a value to add");
            return;
        }
        for(String purchase : consumer.getBehaviorPattern().getPurchaseHistory())
        {
            if(purchase.equals(purchaseElement))
            {
                JOptionPane.showMessageDialog(null, "Category already added");
                return;
            }
        }
        consumer.getBehaviorPattern().getPurchaseHistory().add(purchaseElement);
        populatePurchaseHistory();
        JOptionPane.showMessageDialog(null, "Purchase History Added");
    }//GEN-LAST:event_addPurchasejButtonActionPerformed

    private void updatejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatejButtonActionPerformed
        
        try
        {
            int clickHistory = Integer.parseInt(clickHistoryjTextField.getText());
            if(clickHistory > 100)
            {
                JOptionPane.showMessageDialog(null, "Click History is per 100 ad views");
                return;
            }
            consumer.getBehaviorPattern().setClickHistory(clickHistory);
            populateClickHistory();
            JOptionPane.showMessageDialog(null, "Click History Added");
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null, "Please enter in number format");
        }
    }//GEN-LAST:event_updatejButtonActionPerformed

    private void backJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backJButtonActionPerformed

        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_backJButtonActionPerformed


