    private void populateBrowsingHistoryList()
    {
        DefaultListModel model = new DefaultListModel();
        model.clear();
        
        for(String browse : consumer.getBehaviorPattern().getBrowsingHistory())
        {
            model.addElement(browse);
        }
        browsingHistoryjList.setModel(model);
    }
    
    private void populatePurchaseHistory()
    {
        DefaultListModel model = new DefaultListModel();
        model.clear();
        
        for(String purchase : consumer.getBehaviorPattern().getPurchaseHistory())
        {
            model.addElement(purchase);
        }
        purchaseHistoryjList.setModel(model);
    }
    
    private void populateClickHistory()
    {
        clickHistoryjTextField.setText(String.valueOf(consumer.getBehaviorPattern().getClickHistory()));
    }

