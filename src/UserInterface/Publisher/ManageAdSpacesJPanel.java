    private void addAdSpacejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addAdSpacejButtonActionPerformed

        try
        {
            float height = Float.parseFloat(minHeightjTextField.getText().trim());
            float width = Float.parseFloat(minWidthjTextField.getText().trim());
            String position = (String) positionjComboBox.getSelectedItem();
            float basePrice = Float.parseFloat(basePricejTextField.getText().trim());
            int hitRate = Integer.parseInt(pageHitRatejTextField.getText().trim());
            if(position.equalsIgnoreCase("select"))
            {
                JOptionPane.showMessageDialog(null, "Please select an Ad Position from the list");
                return;
            }
            
            for(AdSpace ad : enterprise.getAdSpaceInventory().getAdSpaceList())
            {
                if(ad.getPosition().equalsIgnoreCase(position))
                {
                    JOptionPane.showMessageDialog(null, "Ad Space already added for that position");
                    return;
                }
            }
            
            enterprise.getAdSpaceInventory().createAdSpace(height, width, position, basePrice, hitRate);
            JOptionPane.showMessageDialog(null, "Ad Space Added");
            populateTable();
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null, "Please enter Ad Dimensions in correct format");
        }
    }//GEN-LAST:event_addAdSpacejButtonActionPerformed

