    private void populateTable()
    {
        DefaultTableModel model = (DefaultTableModel) adInventoryjTable.getModel();
        model.setRowCount(0);
        
        for(AdSpace ad : enterprise.getAdSpaceInventory().getAdSpaceList())
        {
            Object[] row = new Object[4];
            row[0] = ad;
            row[1] = ad.getPosition();
            row[2] = ad.getHeight() + "/" + ad.getWidth();
            row[3] = ad.getBasePrice();
            
            model.addRow(row);
        }
    }

